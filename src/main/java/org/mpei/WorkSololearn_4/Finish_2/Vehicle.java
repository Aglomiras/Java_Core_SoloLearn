package org.mpei.WorkSololearn_4.Finish_2;

public class Vehicle {
    private String color;

    // Getter
    public String getColor() {
        return color;
    }

    // Setter
    public void setColor(String c) {
        this.color = c;
    }
    /**
     * Метод getter возвращает значение атрибута.
     * Метод setter принимает параметр и присваивает его к атрибуту.
     * Ключевое слово this используется для ссылки на текущий объект.
     * Проще говоря, this.color является атрибутом color текущего объекта.
     *
     * Теперь, когда наши методы getter и setter были определены, мы можем использовать их в нашем main:
     * */
}
