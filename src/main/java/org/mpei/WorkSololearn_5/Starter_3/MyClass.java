package org.mpei.WorkSololearn_5.Starter_3;

public class MyClass {
    public static final double PI = 3.14;
    public static void main(String[ ] args) {
        System.out.println(PI);
    }
    /**
     * PI теперь является константой. Любая попытка присвоить ей значение вызовет ошибку.
     * Методы и классы могут быть также обозначены ключевым словом final. Это используется для защиты методов,
     * чтобы они не могли быть переписаны, и для классов, чтобы нельзя было создать подклассы.
     * */
}
